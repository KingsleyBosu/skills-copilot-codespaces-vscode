// Create web server
// Create a route for the path /comments
// Send the following JSON data as a response: { "yourComment": "This is my first comment" }
const express = require('express');
const app = express();

app.get('/comments', (req, res) => {
  res.json({ "yourComment": "This is my first comment" });
});

app.listen(3000, () => {
  console.log('Listening on port 3000!');
});

